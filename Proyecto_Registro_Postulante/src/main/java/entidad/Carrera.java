package entidad;

public class Carrera {
	public int codcarre;
	public String nomcarre;
	
	public int getCodcarre() {
		return codcarre;
	}
	public void setCodcarre(int codcarre) {
		this.codcarre = codcarre;
	}
	public String getNomcarre() {
		return nomcarre;
	}
	public void setNomcarre(String nomCarre) {
		this.nomcarre = nomCarre;
	}
}
